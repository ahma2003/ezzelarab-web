window.addEventListener('load', () => {
  setTimeout(() => {
    const loader = document.getElementById('page-loader');
    if (loader && !loader.classList.contains('hidden')) {
      loader.classList.add('hidden');
    }
  }, 3000);
});

function initLoader() {
  const loader = document.getElementById('page-loader');
  if (!loader) return;
  setTimeout(() => { loader.classList.add('hidden'); }, 800);
  if (document.readyState === 'complete') {
    setTimeout(() => { loader.classList.add('hidden'); }, 100);
  }
}

function initNav() {
  const navbar = document.getElementById('navbar');
  const hamburger = document.querySelector('.nav-hamburger');
  const mobileMenu = document.querySelector('.nav-mobile-menu');
  const mobileLinks = document.querySelectorAll('.nav-mobile-menu a');

  if (!navbar) return;

  const handleScroll = () => {
    if (window.scrollY > 60) {
      navbar.classList.add('scrolled');
      navbar.classList.remove('transparent');
    } else {
      navbar.classList.remove('scrolled');
      navbar.classList.add('transparent');
    }
  };
  window.addEventListener('scroll', handleScroll, { passive: true });
  handleScroll();

  if (hamburger && mobileMenu) {
    hamburger.addEventListener('click', () => {
      hamburger.classList.toggle('open');
      mobileMenu.classList.toggle('open');
      document.body.style.overflow = mobileMenu.classList.contains('open') ? 'hidden' : '';
    });
    mobileLinks.forEach(a => {
      a.addEventListener('click', () => {
        hamburger.classList.remove('open');
        mobileMenu.classList.remove('open');
        document.body.style.overflow = '';
      });
    });
  }

  document.querySelectorAll('a[href^="#"]').forEach(a => {
    a.addEventListener('click', e => {
      const id = a.getAttribute('href');
      if (id === '#') return;
      const target = document.querySelector(id);
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });
}

function initLang() {
  applyLang(currentLang, false);
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.addEventListener('click', () => {
      currentLang = currentLang === 'en' ? 'ar' : 'en';
      localStorage.setItem('ezzelarab_lang', currentLang);
      applyLang(currentLang, true);
    });
  });
}

function applyLang(lang, animate) {
  const t = TRANSLATIONS[lang];
  const body = document.body;

  if (animate) {
    body.style.opacity = '0';
    body.style.transition = 'opacity 0.25s ease';
    setTimeout(() => {
      _applyTranslations(t, lang);
      body.style.opacity = '1';
    }, 250);
  } else {
    _applyTranslations(t, lang);
  }
}

function _applyTranslations(t, lang) {
  if (lang === 'ar') {
    document.documentElement.setAttribute('lang', 'ar');
    document.documentElement.setAttribute('dir', 'rtl');
    document.title = 'عيادات عز العرب | Ezzelarab Clinics';
  } else {
    document.documentElement.setAttribute('lang', 'en');
    document.documentElement.setAttribute('dir', 'ltr');
    document.title = 'Ezzelarab Clinics | عيادات عز العرب';
  }

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' && el.type === 'submit') {
        el.value = t[key];
      } else if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key] ?? el.innerHTML;
      }
    }
  });

  const title3 = document.querySelector('[data-i18n="hero_title_3"]');
  const line2wrap = document.querySelector('.hero-line2-wrap');
  if (title3) {
    const isEmpty = !t['hero_title_3'];
    title3.style.display = isEmpty ? 'none' : '';
    if (line2wrap) {
      line2wrap.style.display = isEmpty && !t['hero_title_2'] ? 'none' : '';
    }
  }

  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.textContent = t['lang_label'];
  });

  const docSelect = document.getElementById('doctor-select');
  if (docSelect) {
    const opts = docSelect.querySelectorAll('option');
    const keys = ['f_d_opt', 'f_d1', 'f_d2', 'f_d3'];
    opts.forEach((opt, i) => {
      if (keys[i] && t[keys[i]]) opt.textContent = t[keys[i]];
    });
  }

  if (typeof initScrollHints === 'function') {
    initScrollHints();
  }
}

function initReveal() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
}

function initBookingForm() {
  const form = document.getElementById('booking-form');
  if (!form) return;

  form.addEventListener('submit', (e) => {
    e.preventDefault();
    const t = TRANSLATIONS[currentLang];

    const name    = form.querySelector('#f-name')?.value.trim() || '';
    const phone   = form.querySelector('#f-phone')?.value.trim() || '';
    const doctor  = form.querySelector('#doctor-select')?.value || '';
    const date    = form.querySelector('#f-date')?.value || '';
    const notes   = form.querySelector('#f-notes')?.value.trim() || '';

    const doctorName = doctor || (currentLang === 'ar' ? 'غير محدد' : 'Not specified');

    let msg;
    if (currentLang === 'ar') {
      msg = `مرحباً، أرغب في حجز موعد:\n👤 الاسم: ${name}\n📞 الهاتف: ${phone}\n👨‍⚕️ الطبيب: ${doctorName}\n📅 التاريخ المفضل: ${date || 'مرن'}\n📝 ملاحظات: ${notes || 'لا يوجد'}`;
    } else {
      msg = `Hello, I'd like to book an appointment:\n👤 Name: ${name}\n📞 Phone: ${phone}\n👨‍⚕️ Doctor: ${doctorName}\n📅 Preferred Date: ${date || 'Flexible'}\n📝 Notes: ${notes || 'None'}`;
    }

    const waURL = `https://wa.me/201500660183?text=${encodeURIComponent(msg)}`;
    window.open(waURL, '_blank');
  });
}

